/**
 * テキスト内の難読語をひらがな化
 */
export function applyYomigana(text) {
  if (!text || typeof text !== 'string') return text;
  let result = text;
  for (const [kanji, yomi] of YOMIGANA_DICT) {
    if (result.includes(kanji)) {
      result = result.split(kanji).join(yomi);
    }
  }
  return result;
}
